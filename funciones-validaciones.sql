CREATE FUNCTION F_VALIDAR_DIA
(
@dia NUMERIC
)
RETURNS NUMERIC
AS
BEGIN
	IF @dia BETWEEN 1 AND 7
		BEGIN 
			RETURN 1
		END
			RETURN 0
	
END
GO

SELECT dbo.F_VALIDAR_DIA(7)
GO

--DROP FUNCTION dbo.F_VALIDAR_DIA

CREATE FUNCTION F_VALIDAR_HORARIO
(
@hora VARCHAR(MAX)
)
RETURNS NUMERIC
AS
BEGIN
	IF @hora LIKE '[0-9][0-9]:[0-9][0-9]-[0-9][0-9]:[0-9][0-9]' 
		BEGIN 
			RETURN 1
		END
			RETURN 0
	
END
GO

SELECT dbo.F_VALIDAR_HORARIO('09:00-10:40')
GO

