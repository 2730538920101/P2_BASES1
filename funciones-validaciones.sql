CREATE FUNCTION F_VALIDAR_CICLO
(
@ciclo VARCHAR(MAX)
)
RETURNS NUMERIC
AS
BEGIN
	IF @ciclo LIKE '1S' OR @ciclo LIKE '2S' OR @ciclo LIKE 'VJ' OR @ciclo LIKE 'VD' 
		BEGIN 
			RETURN 1
		END
			RETURN 0
	
END
GO

SELECT dbo.F_VALIDAR_CICLO('VD')
GO
--DROP FUNCTION dbo.F_VALIDAR_CICLO

