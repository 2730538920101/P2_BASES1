CREATE FUNCTION F_VALIDAR_CORREO
(
@correo VARCHAR(MAX)
)
RETURNS NUMERIC
AS
BEGIN
	IF @correo LIKE '%@%.com' 
		BEGIN 
			RETURN 1
		END
			RETURN 0
	
END
GO

SELECT dbo.F_VALIDAR_CORREO('carlosmartz1995@gmail.com')
GO
--DROP FUNCTION dbo.F_VALIDAR_CORREO



CREATE FUNCTION F_VALIDAR_PALABRA
(
@palabra VARCHAR(MAX)
)
RETURNS NUMERIC
AS
BEGIN
	IF @palabra NOT LIKE '%[0-9_\.\@\-]%' 
		BEGIN 
			RETURN 1
		END
			RETURN 0
	
END
GO

SELECT dbo.F_VALIDAR_PALABRA('HOLA COMO ESTAS')
GO

--DROP FUNCTION dbo.F_VALIDAR_PALABRA

